## 8-Escribir un programa que pida al usuario una cadena de caracteres y muestre
## por pantalla si contiene la letra "a".

cadena = input("Ingrese una cadena de caracteres ")

if "a" in cadena:
    print("La cadena de caracteres si contiene la letra a")
else:
    print("La cadena de caracteres no contienen la letra a")
